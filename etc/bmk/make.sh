#!/bin/bash

#======================================================
# functions
#======================================================
f_titles() {
  grep -h '^\[' *.txt | cut -d ' ' -f 1
}

f_lines() {
  wc -l *.txt
}

#------------------------------------------------------
f_help() {
  echo "titles"
  echo "lines"
  echo "help"
}

f_default() {
  f_help
}

#======================================================
# main
#======================================================
func_name=${1:-"default"}
eval "f_$func_name"


