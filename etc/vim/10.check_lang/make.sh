#!/bin/bash

#======================================================
# variables
#======================================================

#======================================================
# functions
#======================================================
f_cpp() {
  clang++ -std=c++17 a.cpp && ./a.out
}

f_py() {
  python ./a.py
}

f_vim() {
  /usr/bin/vim -u a.vim -c "quit"
}

f_sh() {
  bash ./a.sh
}

#------------------------------------------------------
f_all() {
  echo "--- [cpp] ---"
  f_cpp
  echo "--- [py] ---"
  f_py
  echo "--- [vim] ---"
  f_vim
  echo "--- [sh] ---"
  f_sh
}

#------------------------------------------------------
f_help() {
  echo "all"
  echo "cpp"
  echo "py"
  echo "vim"
  echo "sh"
  echo "help"
}

f_default() {
  f_all
}

#======================================================
# main
#======================================================
func_name=${1:-"default"}
shift
eval "f_$func_name $@"
