#!/bin/bash

#======================================================
# variables
#======================================================

#======================================================
# functions
#======================================================
f_cpp() {
  clang++ -std=c++17 a.cpp && ./a.out
}

f_py() {
  python ./a.py
}

f_vim() {
  echo
}

f_sh() {
  bash ./a.sh
}

#------------------------------------------------------
f_help() {
  echo "cpp"
  echo "py"
  echo "vim"
  echo "sh"
  echo "help"
}

f_default() {
  f_help
}

#======================================================
# main
#======================================================
func_name=${1:-"default"}
shift
eval "f_$func_name $@"

